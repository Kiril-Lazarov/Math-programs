# Глава IV — Защита и подкрепа
#  Член 18 — Общи задължения
#  1 Страните предприемат необходимите законодателни или други мерки за защита на
# всички жертви от последващи актове на насилие.
#  2 Страните предприемат необходимите законодателни или други мерки в съответствие
# с вътрешното законодателство за осигуряване на подходящи механизми за ефективно
# сътрудничество между всички релевантни държавни агенции, включително от
# съдебната област, прокуратура, правоприлагащи органи, местни и регионални власти,
# както и неправителствени организации и други подходящи организации и органи, за
# защита и подкрепа на жертвите и свидетелите на всички форми на насилие, обхванати
# от настоящата Конвенция, включително чрез насочване към общи и специализирани
# услуги за подкрепа като посочените в членове 20 и 22 от настоящата Конвенция.
#  3 Страните гарантират, че предприетите на основание на настоящата глава мерки:
#  – се базират на основано на пола разбиране на насилието над жени и домашното
# насилие и са насочени към правата на човека и безопасността на жертвите;
#  – се базират на интегриран подход, който отчита връзката между жертви,
# извършители, деца и тяхното по-широко социално обкръжение;
#  – са насочени към избягване на вторична виктимизация;
#  – са насочени към овластяване и икономическа независимост на жените жертви на
# насилие;
#  – предвиждат, когато е уместно, разполагане на съвкупност от услуги за защита и
# подкрепа на едно и също място;
#  – са насочени към специалните нужди на уязвимите лица, включително деца
# жертви, и са достъпни за тях.
#  4 Предоставянето на услуги не бива да зависи от желанието на жертвата да подаде
# жалба или да свидетелства срещу извършител.
#  5 Страните предприемат необходимите мерки за осигуряване на консулска и друга
# защита и подкрепа за своите граждани и за други жертви, които имат право на такава
# защита, в съответствие с техните задължения съгласно международното право.
