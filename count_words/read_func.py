# незабавна, краткосрочна и дългосрочна подкрепа за всички жертви, пострадали от
# някой от актовете на насилие, обхванати от настоящата Конвенция.
#  2 Страните осигуряват или уреждат специализирани услуги за подкрепа за жени за
